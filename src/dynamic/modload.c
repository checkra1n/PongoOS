#define PONGO_EXPORT_RAW(name, value)       \
__asm__                                     \
(                                           \
    ".section __TEXT, __cstring\n"          \
    "1:\n"                                  \
    "    .asciz \"_" #name "\"\n"           \
    ".section __DATA, __pongo_exports\n"    \
    ".align 4\n"                            \
    "    .8byte 1b\n"                       \
    "    .8byte " #value "\n"               \
)
